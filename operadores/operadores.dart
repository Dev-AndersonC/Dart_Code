void main() {
  int num = 1;
  print(5 + 2); // Adição
  print(5 - 2); // Subtração
  print(5 * 2); // Multiplicação
  print(5 / 2); // Divisão
  print(5 % 2); // Resto da divisão
  print(6 ~/ 2); // Retorna o grupo da divisão
  print(num++); // Incrementa depois do print
  print(++num); //Incrementa antes do print
  print(num--); // Subtrai depois do print
  print(--num); // Subtrai antes do print
}
